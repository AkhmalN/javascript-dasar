// for Loop

for (let i = 0; i < 5; i++) {
   console.log(i);
}

// for of Loop

let myArray = ["Luke", "Han", "Chewbacca", "Leia"];

for (const arrayItem of myArray) {
   console.log(arrayItem)
}

// While and do while

let i = 1;

while (i <= 100) {
   console.log(i);
   i++;
}


let i = 1;

do {
   console.log(i);
   i++;
} while (i <= 100);

// infinite Loops

let i = 1;

while (i <= 5) {
   console.log(i);
}